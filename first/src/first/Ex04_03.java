package first;

public class Ex04_03 {
public static void main(String[] args) {
	int a = 10;
	a++;
	System.out.println(a);
	a--;
	System.out.println(a);
	a += 5;
	System.out.println(a);
	a -= 5;
	System.out.println(a);
	a *= 5;
	System.out.println(a);
	a /=5;
	System.out.println(a);
	a %=5;
	System.out.println(a);
	a= 10;
	a ++;
	System.out.println("a : " + a);
	System.out.println("a++ : " + a++);
	System.out.println("a : " + a);
	System.out.println("++a : "+ ++a);
	
}
}
